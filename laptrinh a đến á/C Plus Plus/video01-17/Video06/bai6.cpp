#include <iostream>

using namespace std;
int main()
{
  // khởi tạo biến
  int a = 10;
  int b = 3;
  // phép cộng
  cout << "a+b= " << a + b << endl;

  // phép trừ
  cout << "a-b= " << a - b << endl;

  // phép nhân
  cout << "a*b= " << a * b << endl;

  // phép chia
  cout << "a/b= " << a / b << endl;

  // phép chia lấy dư
  cout << "a%b= " << a % b << endl;

  // khai báo biến
  double kq = (double)a / b;
  cout << "a/b= " << kq << endl;
}
